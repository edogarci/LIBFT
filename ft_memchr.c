#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	pos;

	pos = 0;
	while (pos < n)
	{
		if (((char *)s)[pos] == (char)c)
			return ((void *)(&((char *)s)[pos]));
		pos++;
	}
	return (NULL);
}
